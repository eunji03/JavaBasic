package ch03;

public class PlusMinus {

	public static void main(String[] args) {
		
		int num = 10;
		
		System.out.println(+num);
		System.out.println(-num);
		System.out.println(num);

		num = -num;
		System.out.println(num);
		
		byte b = 1;
		System.out.println(+b);
		System.out.println(-b);
		System.out.println(b);
		
		long l = 2;
		System.out.println(+l);
		System.out.println(+l);
		System.out.println(l);

	}

}
